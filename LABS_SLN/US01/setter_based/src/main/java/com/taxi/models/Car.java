package com.taxi.models;

public class Car {
   private String model;
   private String type;

   public void setModel(String model) {
      this.model = model;
   }

   public void setType(String type) {
      this.type = type;
   }

   public String getModel() {
      return model;
   }

   public String getType() {
      return type;
   }
}
